# While Loop: Loops through a block of code as long as a specified condition is true.
# Syntax:
'''
while something_is_true:
  # Do this repeatedly. 
''' 

n = 1
while n < 10 :
  print(n)
  n += 1 


# Use 'not' : This is equal to '!='
# Example:
'''
while not 1  > 2 : 
    print("1 is not greater than 2") # This will be printed 
'''
# 'not' can be used with 'if' and 'while' statements.


